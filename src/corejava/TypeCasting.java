package corejava;

public class TypeCasting {
	
	public static void main(String[] args) {
		//implicit casting
		byte b = 123;
		short s = b;
		
		int it = s;
		
		float f = it;
		double d = f;
		
		//explicit casting
		double db = 7464757.55652222;
		float ft = (float)db;
		long ld = (long)db;
		int id = (int)db;
		
		
	}

}
